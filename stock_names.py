def get_stock_name(stock_id):
    """根據股票代碼取得中文名稱，多層備援"""
    name = TAIWAN_STOCK_NAMES.get(stock_id)
    if name:
        return name
    # yfinance 動態備援（僅在 TAIWAN_STOCK_NAMES 找不到時觸發）
    try:
        import yfinance as yf
        for suffix in ['.TW', '.TWO']:
            info = yf.Ticker(f'{stock_id}{suffix}').fast_info
            _n = getattr(info, 'company_name', None)
            if _n and _n != f'{stock_id}{suffix}':
                return _n
    except Exception:
        pass
    return stock_id  # 最終 fallback